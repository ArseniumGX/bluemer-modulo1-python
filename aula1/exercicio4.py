""" 04 
Elabore um programa que recebe dois valores inteiros e mostra a soma desses valores

Exemplo:

Primeiro Valor = 2
Segundo Valor = 3
Soma = 5
"""

valor1 = int(input("Digite o primeiro valor: "))
